#include "ft_printf.h"

void	ft_putnbr(int n, int *len)
{
	if (n == -2147483648)
		ft_putstr("-2147483648", len);
	else
	{
		if (n < 0)
		{
			ft_putchar('-', len);
			n = n * -1;
		}
		if (n > 9)
		{
			ft_putnbr(n / 10, len);
		}
		ft_putchar((n % 10) + 48, len);
	}
}
